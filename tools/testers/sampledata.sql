CREATE OR REPLACE FUNCTION example2.check_order()
RETURNS TRIGGER AS $trig$
DECLARE
    rec RECORD;
    __id BIGINT;
    travel_time FLOAT;
BEGIN
    INSERT INTO example2.locations (location)
    SELECT id_to_geom(NEW.p_id)
    ON CONFLICT DO NOTHING;

    INSERT INTO example2.locations (location)
    SELECT id_to_geom(NEW.d_id)
    ON CONFLICT DO NOTHING;

    SELECT id FROM example2.locations
    WHERE id = NEW.p_id INTO __id;

    SELECT time_hours
    FROM example2.timeMatrix
    WHERE start_vid = NEW.p_id AND end_vid = NEW.d_id INTO travel_time;

    WITH
    a AS (
        SELECT max(hour) FROM timeDependantMultipliers WHERE hour <= NEW.p_tw_open::TIME
    )
    SELECT multiplier FROM timeDependantMultipliers, a WHERE hour = max INTO NEW.multiplier;

    IF (NEW.multiplier IS NULL) THEN
        RAISE WARNING '%', NEW.p_tw_open::TIME;
        NEW.multiplier := 1::FLOAT;
    END IF;

    assert(NEW.multiplier IS NOT NULL);

    NEW.d_tw_open  := NEW.p_tw_open + (travel_time || ' hours'::text)::interval;
    NEW.d_tw_close := NEW.p_tw_open + '00:20:00'::INTERVAL  + ((1.82 * travel_time) || ' hours'::text)::interval;

    NEW.d_open  := EXTRACT(EPOCH FROM(NEW.d_tw_open))::BIGINT;
    NEW.d_close := EXTRACT(EPOCH FROM(NEW.d_tw_close))::BIGINT;

    RETURN NEW;
END;
$trig$ LANGUAGE plpgsql;

