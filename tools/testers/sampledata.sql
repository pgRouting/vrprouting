CREATE OR REPLACE FUNCTION example2.geom_to_id(
    location geometry
)
RETURNS BIGINT AS
$BODY$
    SELECT CONCAT(ROUND(10000 * public.ST_Y(location)), ROUND(10000 * public.ST_X(location)))::BIGINT;
$BODY$
LANGUAGE SQL IMMUTABLE STRICT;

CREATE OR REPLACE FUNCTION example2.id_to_geom(
    id BIGINT
)
RETURNS geometry AS
$BODY$
    SELECT
        public.ST_SetSRID(
            public.ST_Point(
                (id - id/10000000*10000000)::FLOAT/10000::FLOAT,
                (id/10000000)::FLOAT/10000::FLOAT),
            4326);
$BODY$
LANGUAGE SQL IMMUTABLE STRICT;

