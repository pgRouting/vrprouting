CREATE OR REPLACE FUNCTION example2.tdm(day TIMESTAMP, OUT start_time TIMESTAMP, OUT multiplier FLOAT)
RETURNS SETOF RECORD AS
$BODY$
  SELECT day + hour, multiplier
  FROM example2.timeDependantMultipliers;
$BODY$
LANGUAGE SQL VOLATILE STRICT;

CREATE OR REPLACE FUNCTION example2.tdm(day_from TIMESTAMP, day_to TIMESTAMP, OUT start_time TIMESTAMP, OUT multiplier FLOAT)
RETURNS SETOF RECORD AS
$BODY$
DECLARE
  day TIMESTAMP;
BEGIN
  FOR day IN SELECT * FROM generate_series(day_from, day_to, '1 day'::interval) LOOP
    RETURN QUERY
      SELECT * FROM example2.tdm(day);
  END LOOP;
END;
$BODY$
LANGUAGE plpgsql VOLATILE STRICT;

CREATE OR REPLACE FUNCTION example2.tdm_raw(day TIMESTAMP, OUT start_value BIGINT, OUT multiplier FLOAT)
RETURNS SETOF RECORD AS
$BODY$
  SELECT EXTRACT(EPOCH FROM (day + hour))::BIGINT AS start_val, multiplier
  FROM example2.timeDependantMultipliers;
$BODY$
LANGUAGE SQL VOLATILE STRICT;

