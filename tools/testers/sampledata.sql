-- DROP TABLE IF EXISTS example2.depot CASCADE;
CREATE TABLE IF NOT EXISTS example2.depot (
    id          BIGINT GENERATED ALWAYS AS (geom_to_id(_location)) STORED PRIMARY KEY,
    _location   geometry(Point,4326) NOT NULL
    );

-- DROP TABLE IF EXISTS example2.timeMatrix CASCADE;
CREATE TABLE IF NOT EXISTS example2.timeMatrix(
    start_vid   BIGINT REFERENCES locations(id) NOT NULL,
    end_vid     BIGINT REFERENCES locations(id) NOT NULL,
    time_hours  FLOAT NOT NULL,
    travel_time INTERVAL GENERATED ALWAYS AS (make_interval(secs => time_hours * 3600)) STORED,
    agg_cost    INTEGER  GENERATED ALWAYS AS ((time_hours * 3600)::INTEGER) STORED,
    assigned    BOOL DEFAULT false NOT NULL,
    PRIMARY KEY (start_vid, end_vid)
    );

