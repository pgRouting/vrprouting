CREATE TABLE IF NOT EXISTS example2.locations (
    id      BIGINT GENERATED ALWAYS AS (geom_to_id(location)) STORED PRIMARY KEY,
    location    geometry(Point,4326) NOT NULL,
    -- columns calculated with triggers or generated columns
    latitude    FLOAT GENERATED ALWAYS AS (ST_Y(location)) STORED,
