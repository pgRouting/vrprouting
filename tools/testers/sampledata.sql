CREATE OR REPLACE FUNCTION example2.set_estimated()
RETURNS TRIGGER AS $trig$
DECLARE
rec RECORD;
BEGIN
    SELECT * FROM example2.timematrix t
    WHERE t.start_vid = NEW.end_vid AND t.end_vid = NEW.start_vid
    INTO rec;

    IF (rec.start_vid IS NULL) THEN
        -- the oposite direction calculation does not exist
        -- insert the oposite direction as estimation
        INSERT INTO example2.timematrix (start_vid, end_vid, time_hours, assigned)
        SELECT NEW.end_vid, NEW.start_vid, NEW.time_hours, false;
    ELSE
        IF (NEW.assigned = true AND rec.assigned = false) THEN
            UPDATE example2.timematrix AS t
            SET time_hours = NEW.time_hours
            WHERE t.start_vid = NEW.end_vid AND t.end_vid = NEW.start_vid;
        END IF;
    END IF;

    RETURN NEW;
END;
$trig$ LANGUAGE plpgsql;


CREATE TRIGGER tgr_timeMatrix_03
AFTER INSERT OR UPDATE ON example2.timeMatrix
FOR EACH ROW EXECUTE PROCEDURE example2.set_estimated();





