CREATE TABLE IF NOT EXISTS example2.physical_vehicles (
    id              BIGINT PRIMARY KEY,
    capacity        INTEGER,
    depot_id        BIGINT REFERENCES example2.depot(id) NOT NULL
    );



-- DROP TABLE IF EXISTS example2.vehicles CASCADE;
CREATE TABLE IF NOT EXISTS example2.vehicles(
  id          BIGINT PRIMARY KEY,
  vehicle_id  BIGINT REFERENCES example2.physical_vehicles(id),
  capacity    BIGINT NOT NULL CHECK(capacity > 0),
  stops       BIGINT[],

  s_id        BIGINT NOT NULL REFERENCES example2.depot(id),
  s_tw_open   TIMESTAMP NOT NULL,
  s_tw_close  TIMESTAMP NOT NULL,
  s_t_service INTERVAL DEFAULT '00:00:00'::INTERVAL CHECK(s_t_service >= '00:00:00'::INTERVAL),

  e_id        BIGINT NOT NULL REFERENCES example2.depot(id),
  e_tw_open   TIMESTAMP GENERATED ALWAYS AS (s_tw_open) STORED,
  e_tw_close  TIMESTAMP GENERATED ALWAYS AS (s_tw_close) STORED,
  e_t_service INTERVAL DEFAULT '00:00:00'::INTERVAL CHECK(e_t_service >= '00:00:00'::INTERVAL),

  -----------------------
  -- Data needed with raw function
  -----------------------
  s_open      BIGINT GENERATED ALWAYS AS (EXTRACT(EPOCH FROM(s_tw_open))::BIGINT) STORED,
  s_close     BIGINT GENERATED ALWAYS AS (EXTRACT(EPOCH FROM(s_tw_close))::BIGINT) STORED,
  s_service   BIGINT GENERATED ALWAYS AS (EXTRACT(EPOCH FROM(s_t_service))::BIGINT) STORED,

  e_open      BIGINT GENERATED ALWAYS AS (EXTRACT(EPOCH FROM(s_tw_open))::BIGINT) STORED,
  e_close     BIGINT GENERATED ALWAYS AS (EXTRACT(EPOCH FROM(s_tw_close))::BIGINT) STORED,
  e_service   BIGINT GENERATED ALWAYS AS (EXTRACT(EPOCH FROM(e_t_service))::BIGINT) STORED,


  CHECK(s_tw_open < s_tw_close),
  CHECK(e_tw_open < e_tw_close),
  CHECK(s_tw_open <= s_tw_open)
);

-- DROP TABLE IF EXISTS example2.shipments CASCADE;
CREATE TABLE IF NOT EXISTS example2.shipments (
  id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  amount          BIGINT NOT NULL CHECK(amount > 0),
  -- Pickup
  p_id            BIGINT NOT NULL REFERENCES locations(id),
  p_tw_open       TIMESTAMP NOT NULL,
  p_tw_close      TIMESTAMP GENERATED ALWAYS AS (p_tw_open + '00:20:00'::INTERVAL) STORED,
  p_t_service     INTERVAL DEFAULT '00:00:00'::INTERVAL CHECK(p_t_service >= '00:00:00'::INTERVAL),

  -- Delivery
  d_id            BIGINT NOT NULL REFERENCES locations(id),
  d_tw_open       TIMESTAMP NOT NULL,
  d_tw_close      TIMESTAMP NOT NULL,
  d_t_service     INTERVAL DEFAULT '00:00:00'::INTERVAL CHECK(d_t_service >= '00:00:00'::INTERVAL),

  -----------------------
  -- Data needed with raw function
  -----------------------
  -- Pickup
  p_open          BIGINT GENERATED ALWAYS AS (EXTRACT(EPOCH FROM(p_tw_open))::BIGINT) STORED,
  p_close         BIGINT GENERATED ALWAYS AS (EXTRACT(EPOCH FROM(p_tw_open + '00:20:00'::INTERVAL))::BIGINT) STORED,
  p_service       BIGINT GENERATED ALWAYS AS (EXTRACT(EPOCH FROM(p_t_service))::BIGINT) STORED,

  -- Delivery
  d_open          BIGINT NOT NULL,
  d_close         BIGINT NOT NULL,
  d_service       BIGINT GENERATED ALWAYS AS (EXTRACT(EPOCH FROM(d_t_service))::BIGINT) STORED,

  -- is this been used?
  multiplier     FLOAT NOT NULL,    -- updated with trigger

  -- TODO
  -- Booking date should be handled with skills
  -- vehicle: for each running date set a skill_id
  -- shipments: if the booking date = to the running date of the vehicle then get the skill_id of the vehicle
  booking_date    TIMESTAMP,
  CHECK (p_tw_open <= p_tw_close),
  CHECK (p_tw_open <= d_tw_open),
  CHECK (d_tw_open <= d_tw_close),
  CHECK (booking_date <= p_tw_open),
  CHECK (p_id != d_id)
  );



