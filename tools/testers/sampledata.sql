CREATE OR REPLACE FUNCTION example2.check_locations()
RETURNS trigger AS $trig$
    BEGIN
        assert(NEW.id IS NOT NULL);

        -- its a new location, adding data to the timematrix table
        -- as estimated
        INSERT INTO example2.timeMatrix(start_vid, end_vid, time_hours, assigned)
        SELECT NEW.id, l.id,
            (ST_distance(NEW.location::geography, l.location::geography)/1000)/30.0::float, false
        FROM locations AS l
        ON CONFLICT DO NOTHING;

        -- its a new location, adding data to the timematrix table
        -- as assigned
        INSERT INTO example2.timeMatrix(start_vid, end_vid, time_hours, assigned)
        SELECT NEW.id, NEW.id, 0, true
        ON CONFLICT DO NOTHING;

        RETURN NEW;
    END;
$trig$ LANGUAGE plpgsql;

